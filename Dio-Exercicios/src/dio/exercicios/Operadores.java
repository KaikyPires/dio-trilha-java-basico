package dio.exercicios;

public class Operadores {

    public static void main(String[] args) {
        //ARITIMETICOS
        int a = 10;         int b = 5;
        int soma = a + b;       // Soma
        int subtracao = a - b;  // Subtração
        int multiplicacao = a * b;  // Multiplicação
        int divisao = a / b;    // Divisão
        int resto = a % b;      // Resto da divisão
        //Operadores de Atribuição:
        int c = 10;
        c += 5;  // Equivalente a: c = c + 5;
        c -= 3;  // Equivalente a: c = c - 3;
        c *= 2;  // Equivalente a: c = c * 2;
        c /= 4;  // Equivalente a: c = c / 4;
        c %= 3;  // Equivalente a: c = c % 3;
        //Operadores de Comparação:
        int x = 10;          int y = 5;
        boolean igual = x == y;   // Igual a
        boolean diferente = x != y;  // Diferente de
        boolean maiorQue = x > y;   // Maior que
        boolean menorQue = x < y;   // Menor que
        boolean maiorOuIgual = x >= y;  // Maior ou igual a
        boolean menorOuIgual = x <= y;  // Menor ou igual a
        //Logicos
        boolean p = true;    boolean q = false;
        if(p && q){// E lógico
        }
        boolean ouLogico = p || q;  // Ou lógico
        boolean nao = !p;           // Negação lógica/ vai inverter
        //Incremento ou desencremento
        int num = 5;
        num++;   // Incremento (equivalente a num = num + 1)
        num--;   // Decremento (equivalente a num = num - 1)



    }
    
}
